package Java_Module_2;

public class SwitchEx {
    public static void main(String[] args) {
        int day=3;
        switch(day){
            case 1:
              System.out.println("Monday");
              break;
            case 2:
              System.out.println("Monday");
              break;
            case 3:
              System.out.println("Monday");
              break;
            case 4:
              System.out.println("Monday");
              break;
            case 5:
              System.out.println("Monday");
              break;
            case 6:
              System.out.println("Monday");
              break;
            case 7:
              System.out.println("Monday");
              break;
            default:
              System.out.println("Invalid day: ");
        }
    }
}
